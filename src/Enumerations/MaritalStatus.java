package Enumerations;

/**
 * Represents the marital status of an individual.
 * <p>
 * This enumeration defines whether a person is married or single.
 */
public enum MaritalStatus {
    /**
     * Indicates that the individual is legally married.
     */
    MARRIED,
    /**
     * Indicates that the individual is not married.
     */
    SINGLE
}
